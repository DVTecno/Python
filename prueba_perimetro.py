print("Calculo de area y peri­metro")
alto = float(input("Indica el alto del rectangulo: "))
ancho = float(input("Indica el ancho del rectangulo: "))
area = alto*ancho
perimetro = 2*(alto+ancho)
print(f"El area del rectangulo es {area}")
print(f"El peri­metro del rectangulo es {perimetro}")
pi = 3.1416
radio = float(input("Ingrese el radio del ci­rculo: "))
area = pi*radio**2
perimetro = 2*pi*radio
print(f"El area del ci­rculo es {area}")
print(f"El peri­metro del circulo es {perimetro}")
